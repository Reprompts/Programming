# Question 4: Write a Python program to enter length and breadth of a rectangle and calculate its area.
# Input:
# Length = 10
# Breadth = 5

# Output:
# Area = 50

# Explanation:
# The area of a rectangle is calculated using the formula:
# Area = Length × Breadth
# So, 10 × 5 = 50.

# Asked In Companies:
# Practice Assignment

Length = int(input("Enter the length of the rectangle: "))
Breadth = int(input("Enter the breadth of the reactangle: "))

Area = Length * Breadth

print("The area of the rectangle is:", Area)