# Question 19: Write a Python program that reads a number and displays its cube.
# Input:
# Number = 4

# Output:
# Cube = 64

# Explanation:
# The cube of a number is calculated by multiplying the number by itself three times.
# 4 × 4 × 4 = 64.

# Asked In Companies:
# Practice Assignment

Num = int(input("Enter a number to find its cube: "))
print("The cube of the number is: ", Num ** 3)